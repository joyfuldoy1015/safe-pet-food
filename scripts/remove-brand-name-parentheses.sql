-- 브랜드명에서 괄호와 영문명 제거
-- ============================================
-- 목적: URL 간소화 및 다국어 지원 개선
-- 예: "로얄캐닌(Royal Canin)" → "로얄캐닌"
-- ============================================

-- 브랜드명에서 괄호와 영문명 제거
UPDATE brands
SET name = TRIM(REGEXP_REPLACE(name, '\s*\([^)]*\)\s*$', ''))
WHERE name ~ '\([^)]*\)';

-- 결과 확인
SELECT id, name, manufacturer, country
FROM brands
ORDER BY name;

